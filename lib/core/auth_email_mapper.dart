/// Firebase Authentication بيتطلب إيميل، مش يوزرنيم بسيط زي "admin" أو
/// "ibrahim" اللي التطبيق شغال بيه دلوقتي. الحل المعروف والآمن: بنحوّل
/// اليوزرنيم لإيميل "مصطنع" بدومين ثابت مالوش وجود حقيقي (workshop.local)،
/// بس مقبول تمامًا عند Firebase Auth كصيغة إيميل صحيحة - مفيش هنا أي إيميل
/// حقيقي بيتبعتله رسايل، هو مجرد "معرّف" شكله إيميل عشان يعدّي فاليديشن
/// Firebase الداخلي بس.
///
/// ملحوظة: التحويل ده لازم يكون *متسق* في كل حتة (تسجيل الدخول، إنشاء
/// يوزر جديد، تغيير الباسورد...إلخ) - عشان كده الدالة هنا واحدة بس
/// ومستخدمة من كل مكان، بدل ما كل ملف يعمل نسخته الخاصة وتحصل مشاكل تطابق.
String usernameToAuthEmail(String username) {
  final normalized = username.trim().toLowerCase();
  return '$normalized@workshop.local';
}
